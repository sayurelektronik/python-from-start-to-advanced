# Kamu bisa meminta input dari pengguna menggunakan fungsi input().

# Contoh
nama = input("Masukkan nama kamu: ")
print("Halo,", nama)


# Python selalu menganggap input sebagai string, jadi kamu mungkin perlu mengonversinya ke tipe data lain seperti integer atau float.
# Contoh konversi input:
umur = int(input("Masukkan umur kamu: "))
print("Umur kamu adalah", umur)

